
// WHILE loop
var amount = 0;

// Create the index
var i = 0;

// Check the condition
while ( i < 10 ) {
	amount += 100;
	// Increment the index
	i++;
}

alert("The amount is: " + amount);


// FOR loop
var value = 0;

for (var j = 0; j < 10; j++) {

	value += 100;

}

alert("The value is: " + value);


// DO... WHILE loop
var numb = 0;
var a = 0;

do {
	numb += 100;
	a++;
} while (a < 10);

alert(numb);